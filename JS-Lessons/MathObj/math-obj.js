//Math operator things you can do with it :D

console.log(Math.PI);

//Rounds to nearest integer
console.log(Math.round(4.6));

//Rounds down to nearest integer
console.log(Math.floor(4.6));

//Rounds up to nearest integer
console.log(Math.ceil(4.2));
console.log(Math.ceil(4.9));

//Find largest or smallest numbers
console.log(Math.max(3,4,6,7,20,35,58,27,1,41));
console.log(Math.min(3,4,6,7,20,35,58,27,1,41));
//to do with arrays, must use spread operator
const nums = [1,2,4,6,14,81,95,243,145,9,55];
console.log(Math.max(...nums))

//exponentials math.pow(2,3) is 2^3 which is 8
console.log(Math.pow(2,3));



